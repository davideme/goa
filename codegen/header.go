package codegen

import (
	"goa.design/goa/v3/expr"
	goa "goa.design/goa/v3/pkg"
)

// Header returns a Go source file header section template.
func Header(title, pack string, imports []*ImportSpec, meta expr.MetaExpr) *SectionTemplate {
	value, ok := meta.Last("goa:version:generate")
	generateVersion := !ok || value != "false"
	return &SectionTemplate{
		Name:   "source-header",
		Source: headerT,
		Data: map[string]any{
			"Title":           title,
			"ToolVersion":     goa.Version(),
			"Pkg":             pack,
			"Imports":         imports,
			"GenerateVersion": generateVersion,
		},
	}
}

// AddImport adds imports to a section template that was generated with
// Header.
func AddImport(section *SectionTemplate, imprts ...*ImportSpec) {
	if len(imprts) == 0 {
		return
	}
	var specs []*ImportSpec
	if data, ok := section.Data.(map[string]any); ok {
		if imports, ok := data["Imports"]; ok {
			specs = imports.([]*ImportSpec)
		}
		data["Imports"] = append(specs, imprts...)
	}
}

const (
	headerT = `{{if .Title}}// Code generated by goa{{if .GenerateVersion}} {{.ToolVersion}}{{end}}, DO NOT EDIT.
//
// {{.Title}}
//
// Command:
{{comment commandLine}}

{{end}}package {{.Pkg}}

{{if .Imports}}import {{if gt (len .Imports) 1}}(
{{end}}{{range .Imports}}	{{.Code}}
{{end}}{{if gt (len .Imports) 1}})
{{end}}
{{end}}`
)
